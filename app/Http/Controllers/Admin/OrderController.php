{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $orders=order::where('status','new')->orWhere('status','process')->orderBy('id','DESC')->get();

        $pays=order::where('status','selesai')->orWhere('status','cancel')->orderBy('id','DESC')->get();

        return view('pages.admin.order.index',compact('orders','pays'));
    }

    /**
